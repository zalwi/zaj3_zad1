class Glass {
    String name;
    String material;
    int capacity;
    int totalCapacity;
    boolean isEmpty;

    public Glass(String name, String material, int capacity, int totalCapacity, boolean isEmpty) {
        this.name = name;
        this.material = material;
        this.capacity = capacity;
        this.totalCapacity = totalCapacity;
        this.isEmpty = isEmpty;
    }
}
